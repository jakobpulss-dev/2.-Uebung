package p2;

public class p2_main { public static void main(String[] args) {
    int i = 5;          // i bekommt einen beliebigen Wert
    boolean b = false;   // b bekommt einen beliebigen Anfangswert

    // Überprüfung, ob i echt größer als 10 ist
    if (i > 10) {
        b = true;        // b wird nur geändert, wenn Bedingung erfüllt ist
    }

    System.out.println("i = " + i);
    System.out.println("b = " + b);
}
};


